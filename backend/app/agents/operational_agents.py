"""Logistics, evacuation, simulation, coordinator — wired to real prediction outputs and LLM."""

from __future__ import annotations

import json
from backend.app.agents.base import AgentContext, AgentRunner, compute_confidence, evidence, STRICT_REALTIME_PROMPT
from backend.app.schemas import AgentResult, GenerationMessage

AGENT_SEQUENCE = [
    "coordinator",
    "knowledge",
    "similarity",
    "risk",
    "prediction",
    "explainability",
    "logistics",
    "evacuation",
    "simulation",
    "validation",
    "planner",
    "decision",
    "reporting",
]

class CoordinatorAgent(AgentRunner):
    agent_name = "coordinator"

    async def run(self, ctx: AgentContext) -> AgentResult:
        confidence = 0.9 if ctx.rag.get("status") == "ok" else 0.2
        return self._result(
            ctx.scenario,
            confidence=confidence,
            reasoning_summary="Orchestrating evidence-backed agent pipeline with mandatory Supabase retrieval.",
            evidence_items=[evidence("coordinator", "Execution plan", str(AGENT_SEQUENCE))],
            recommendations=["Execute all agents in sequence."],
            next_actions=["Retrieve historical disasters."],
            status="completed",
            output={"agent_sequence": AGENT_SEQUENCE, "handoff_to": "knowledge"},
            execution_time_ms=0,
        )


class LogisticsAgent(AgentRunner):
    agent_name = "logistics"

    async def run(self, ctx: AgentContext) -> AgentResult:
        prediction = ctx.outputs.get("prediction", {})
        risk = ctx.outputs.get("risk", {})
        rag = ctx.rag

        if prediction.get("status") == "insufficient_data" or rag.get("status") != "ok":
            return self._result(
                ctx.scenario,
                confidence=0.0,
                reasoning_summary="Logistics requires prediction outputs and retrieved historical evidence.",
                evidence_items=[],
                recommendations=[],
                next_actions=[],
                status="failed",
                output={"status": "insufficient_data"},
                execution_time_ms=0,
            )

        prompt = (
            f"Scenario: {ctx.scenario.disaster_type} in {ctx.scenario.region}.\n"
            f"Predictions: {json.dumps(prediction)}\n"
            f"Risk: {json.dumps(risk)}\n\n"
            f"RAG Context:\n{rag.get('context', '')}\n\n"
            "Generate a logistics resource plan based STRICTLY on the real ML predictions and the retrieved historical disasters. "
            "Output valid JSON exactly matching this structure:\n"
            "{\n"
            '  "resource_plan": {"ambulances": <int>, "rescue_teams": <int>, "medical_kits": <int>, "water_units": <int>, "food_packets": <int>},\n'
            '  "resource_shortfalls": ["<string>"]\n'
            "}"
        )

        try:
            generated = await ctx.generation_client.generate(
                messages=[
                    GenerationMessage(role="system", content=f"{STRICT_REALTIME_PROMPT}\nYou are a strict JSON-only AI disaster logistics planner."),
                    GenerationMessage(role="user", content=prompt),
                ],
                max_tokens=600,
                temperature=0.1,
            )
            data = json.loads(generated.content)
        except Exception as exc:
            return self._result(
                ctx.scenario,
                confidence=0.0,
                reasoning_summary=f"LLM generation failed: {exc}",
                evidence_items=[],
                recommendations=[],
                next_actions=[],
                status="failed",
                output={"status": "failed", "error": str(exc)},
                execution_time_ms=0,
            )

        retrieved = rag.get("retrieved_disasters", [])
        top_sim = float(retrieved[0].get("similarity", 0)) if retrieved else 0.0

        return self._result(
            ctx.scenario,
            confidence=compute_confidence(True, len(retrieved), top_sim, []),
            reasoning_summary="Resource plan generated by LLM analysis of XGBoost predictions and RAG context.",
            evidence_items=[
                evidence("prediction", "Evacuation need", str(prediction.get("predicted_affected", 0))),
                evidence("prediction", "Casualties", str(prediction.get("predicted_deaths", 0))),
            ],
            recommendations=["Stage resources per computed plan."],
            next_actions=["Pass to Evacuation agent."],
            status="completed",
            output={
                "resource_plan": data.get("resource_plan", {}),
                "available_resources": data.get("resource_plan", {}),
                "resource_shortfalls": data.get("resource_shortfalls", []),
                "source_scenario": ctx.scenario.model_dump(),
                "risk_score": risk.get("risk_score"),
            },
            execution_time_ms=0,
        )


